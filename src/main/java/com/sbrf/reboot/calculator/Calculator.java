package com.sbrf.reboot.calculator;

public class Calculator {
    public static int getAddition(int a, int b) {
        return a + b;
    }

    public static int getSubtraction(int a, int b) {
        return a - b;
    }

    public static int getMultiplication(int a, int b) {
        return a * b;
    }

    public static int getDivision(int a, int b) {
        return a / b;
    }

    public static int getBitwiseAnd(int a, int b) {
        return a & b;
    }

    public static int getBitwiseOr(int a, int b) {
        return a | b;
    }

    public static int getBitwiseXor(int a, int b) {
        return a ^ b;
    }
}
